/* Constructor y metodo para agregar nuevos usuarios */

class Usuario {
  constructor(name, username, email, phone, pass) {
    this.id = usuarios.length + 1;
    this.name = name;
    this.username = username;
    this.email = email;
    this.phone = phone;
    this.password = pass;
  }

  //Metodos
  static agregarUsuario() {
    let usuario = new Usuario(
      document.registerUser.name.value,
      document.registerUser.user.value,
      document.registerUser.email.value,
      document.registerUser.phone.value,
      document.registerUser.password.value
    );
    let auxUsuario = JSON.stringify(usuario);
    localStorage.setItem("usuarioRegistrado", auxUsuario);
    usuarios.push(JSON.parse(auxUsuario));

    //Guarda el arreglo de usuarios en el almacenamiento local cada nuevo registro

    localStorage.setItem("usuariosExistentes", JSON.stringify(usuarios));
    alert("¡Usuario registrado!");
  }
}

