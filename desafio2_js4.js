/* Validación de usuarios */

function validateUser(user, pass) {
  for (let i = 0; i < usuarios.length; i++) {
    if (usuarios[i].username == user) {
      console.log("usuario encontrado");
      if (usuarios[i].password == pass) {
        alert("Entraste!");
        localStorage.setItem("usuarioLogueado", JSON.stringify(usuarios[i]));
        document.getElementById("signOffButton").style.display = "flex";
        document.getElementById("signInButton").style.display = "none";
        document.getElementById("registerButton").style.display = "none";

        /* Mensaje de bienvenida */

        let userName = usuarios[i].username;
        welcomeMenssage.style.display = "flex";
        welcomeUserName.innerHTML = `${userName}`;

        setTimeout(() => {
          welcomeMenssage.style.display = "none";
        }, 5000);

        continuarSesion();
      } else {
        //contraseña invalida
        console.log("contraseña invalida");
      }
    } else {
      console.log("usuario no encontrado");
      //usuario no existe
    }
  }
}

